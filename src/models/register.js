const mongoose = require("mongoose");

const employeeSchema = new mongoose.Schema({
  fname: {
    type: String,
    required: true,
  },
  lname: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
});

//Now we need to create a collections
const Register = mongoose.model("Register", employeeSchema);

module.exports = Register;
